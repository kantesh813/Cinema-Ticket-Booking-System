    private void btn_PayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PayActionPerformed
        // TODO add your handling code here:
        if (cmb_CardType.getSelectedIndex() == 0) {
            if ("".equals(txt_HolderName.getText())) {
                JOptionPane.showConfirmDialog(null, "Enter Card Holder Name ", "Card Holder Name Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                return;
            } else if ("".equals(txt_CCV.getText())) {
                JOptionPane.showConfirmDialog(null, "Enter CCV Number ", "CCV Number Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                return;
            } else if ("".equals(txt_CardNumber.getText())) {
                JOptionPane.showConfirmDialog(null, "Enter Card Number ", "Card Number Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            int row = Utility.databaseInteraction.insertPaymentDetails(new Card(0, 0, txt_HolderName.getText(), txt_CCV.getText(), txt_CardNumber.getText()), txt_DrinkQty.getValue(), txt_PopcornQty.getValue());
            if (row > 0) {
                JOptionPane.showConfirmDialog(null, "Order  Placed Successfully.", "Success", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                Utility.cartData.clear();
            } else {
                JOptionPane.showConfirmDialog(null, "Order Not Placed.", "Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            int row = Utility.databaseInteraction.insertPaymentDetails(new Cash(0, 0), txt_DrinkQty.getValue(), txt_PopcornQty.getValue());
            if (row > 0) {
                JOptionPane.showConfirmDialog(null, "Order  Placed Successfully.", "Success", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                Utility.cartData.clear();
            } else {
                JOptionPane.showConfirmDialog(null, "Order Not Placed.", "Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            }
        }
        this.dispose();
        new MainCustomerView().setVisible(true);
    }//GEN-LAST:event_btn_PayActionPerformed

