    public void calculateTotalPrice() {
        double total = Utility.databaseInteraction.calculateTotalCartPrice();
        total += txt_DrinkQty.getValue() * Utility.DRINKPRICE;
        total += txt_PopcornQty.getValue() * Utility.POPCORNPRICE;
        txt_TotalPrice.setText("₪ : " + total);
    }


