    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        // TODO add your handling cod here:
        if (validateEmail() && validatePassword()){
            Utility.currentUsers = Utility.databaseInteraction.validateLoginCredention(txt_email.getText(), txt_password.getText());
            if (Utility.currentUsers == null) {
                JOptionPane.showConfirmDialog(null, "Invalid Username or Password", "Authentication Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            } else {
               if ("Admin".equals(Utility.currentUsers.getClass().getSimpleName())) {
                    this.dispose();
                    new MainAdminView().setVisible(true);
                } else {
                    this.dispose();
                    new MainCustomerView().setVisible(true);

                }
            }
        }


    }//GEN-LAST:event_btn_LoginActionPerformed

