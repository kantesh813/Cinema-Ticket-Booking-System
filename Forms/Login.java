    public boolean validateEmail() {
        String email = txt_email.getText();
        if ("".equals(email)) {
            JOptionPane.showConfirmDialog(null, "Enter Email", "Email Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else {

            String regex = "^(.+)@(.+)$";
            Pattern pattern = Pattern.compile(regex);
            Matcher matcher = pattern.matcher(email);
            if (!matcher.matches()) {
                JOptionPane.showConfirmDialog(null, "Invalid Email", "Email Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);

                return false;
            } else {
                return true;
            }
        }
    }

    public boolean validatePassword() {
        String password = txt_password.getText();
        if ("".equals(password)) {
            JOptionPane.showConfirmDialog(null, "Enter PAssword", "Password Error", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

